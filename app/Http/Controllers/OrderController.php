    public static function getTotal($id){

        $order = Order::find($id);
        $totalPrice = 0;
        foreach($order->items as $item){
            $tItem = $item->price*$item->quantity;
            $totalPrice = $totalPrice+$tItem;
        }

        return $totalPrice;

